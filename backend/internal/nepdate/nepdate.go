// Package nepdate converts between the Bikram Sambat and Gregorian calendars.
//
// The frontend has the same conversion in Dart, and this is deliberately a
// second copy rather than a shared service: the server needs it to know when a
// BS birthday next falls, and a reminder that depends on a round trip to the
// browser is not a reminder at all.
//
// BS month lengths vary from year to year with no formula behind them, so the
// conversion is a lookup table. This one is transcribed from the `nepali_utils`
// Dart package and covers BS 1969 to 2250. Note that only its *table* is
// borrowed: that package's own AD->BS conversion carries a fudge factor
// ("difference += 1" past 1986) and is a day out, which is why both this and
// the Dart side derive AD->BS by searching the trustworthy direction instead.
// See nepdate_test.go, which pins the result against the government gazette.
package nepdate

