// yearDays is the length of a BS year.
func yearDays(year int) int {
	total := 0
	for _, d := range monthDays[year] {
		total += d
	}
	return total
}

// MonthLength returns the number of days in a BS month, or 0 when the year is
// outside the table.
func MonthLength(year, month int) int {
	m, ok := monthDays[year]
	if !ok || month < 1 || month > 12 {
		return 0
	}
	return m[month-1]
}

// ToAD converts a Bikram Sambat date to the Gregorian calendar.
func ToAD(year, month, day int) (time.Time, error) {
	if year < MinYear || year > MaxYear {
		return time.Time{}, fmt.Errorf("BS year %d is outside %d-%d",
			year, MinYear, MaxYear)
	}
	if month < 1 || month > 12 {
		return time.Time{}, fmt.Errorf("BS month %d is out of range", month)
	}
	length := MonthLength(year, month)
	if day < 1 || day > length {
		return time.Time{}, fmt.Errorf(
			"BS %d-%d has %d days, not %d", year, month, length, day)
	}

	days := 0
	for y := MinYear; y < year; y++ {
		days += yearDays(y)
	}
	for m := 1; m < month; m++ {
		days += MonthLength(year, m)
	}
	days += day - 1
	return epoch.AddDate(0, 0, days), nil
}

// FromAD converts a Gregorian date to Bikram Sambat.
//
// Derived by walking the table forward from the epoch rather than by the
// arithmetic the source package uses, which is a day out.
func FromAD(t time.Time) (Date, error) {
	day := time.Date(t.Year(), t.Month(), t.Day(), 0, 0, 0, 0, time.UTC)
	remaining := int(day.Sub(epoch).Hours() / 24)
	if remaining < 0 {
		return Date{}, fmt.Errorf("%s is before the BS table begins",
			day.Format("2006-01-02"))
	}

	year := MinYear
	for {
		if year > MaxYear {
			return Date{}, fmt.Errorf("%s is beyond the BS table",
				day.Format("2006-01-02"))
		}
		length := yearDays(year)
		if remaining < length {
			break
		}
		remaining -= length
		year++
	}

	month := 1
	for remaining >= MonthLength(year, month) {
		remaining -= MonthLength(year, month)
		month++
	}
	return Date{Year: year, Month: month, Day: remaining + 1}, nil
}

// Repeat modes for a yearly event.
const (
	// RepeatNone is a one-off.
	RepeatNone = "none"
	// RepeatAD recurs on the same Gregorian month and day.
	RepeatAD = "ad"
	// RepeatBS recurs on the same Bikram Sambat month and day.
	RepeatBS = "bs"
)

// NextOccurrence returns the next Gregorian date, on or after `from`, that a
// repeating event falls on.
//
// The two calendars disagree about when "next year" is, and an event has to
// say which one it means. A birthday kept on 15 Ashar lands on a different
// Gregorian day every year; a birthday kept on 9 July lands on a different BS
// day every year. Matching on the stored Gregorian month and day — the obvious
// implementation, and a single SQL predicate — quietly gives the wrong day to
// everyone who keeps their birthday by the Nepali calendar.
//
// Lives here rather than in either caller so the handler and the reminder
// sweep cannot drift apart: if they disagreed, an event would be shown on one
// day and reminded about on another.
func NextOccurrence(mode string, date, from time.Time) (time.Time, error) {
	from = midnight(from)
	date = midnight(date)

	switch mode {
	case RepeatNone:
		return date, nil

	case RepeatAD:
		for year := from.Year(); year <= from.Year()+8; year++ {
			when := time.Date(
				year, date.Month(), date.Day(), 0, 0, 0, 0, time.UTC)
			// Go rolls 29 February in a common year into 1 March. Pull it
			// back to the 28th, the way a missing BS day is clamped below.
			if when.Month() != date.Month() {
				when = when.AddDate(0, 0, -when.Day())
			}
			if !when.Before(from) {
				return when, nil
			}
		}
		return time.Time{}, fmt.Errorf(
			"no Gregorian occurrence of %s after %s",
			date.Format("2006-01-02"), from.Format("2006-01-02"))

	case RepeatBS:
		bs, err := FromAD(date)
		if err != nil {
			return time.Time{}, err
		}
		return NextAnniversary(bs, from)
	}
	return time.Time{}, fmt.Errorf("unknown repeat mode %q", mode)
}

func midnight(t time.Time) time.Time {
	return time.Date(t.Year(), t.Month(), t.Day(), 0, 0, 0, 0, time.UTC)
}

// NextAnniversary returns the next Gregorian date on or after `from` on which
// the BS month and day of `of` recur.
//
// This is why the package exists. A birthday kept by the BS calendar does not
// fall on the same Gregorian day each year — it drifts — so "same month and day
// next year" is the wrong question to ask of the database.
//
// A day that does not exist in the target year (BS 32 Ashar, in a year where
// Ashar has 31) falls back to the last day of that month, the way a 29 February
// birthday is kept on the 28th.
func NextAnniversary(of Date, from time.Time) (time.Time, error) {
	from = time.Date(from.Year(), from.Month(), from.Day(), 0, 0, 0, 0, time.UTC)
	start, err := FromAD(from)
	if err != nil {
		return time.Time{}, err
	}
	for year := start.Year; year <= MaxYear; year++ {
		length := MonthLength(year, of.Month)
		if length == 0 {
			continue
		}
		day := of.Day
		if day > length {
			day = length
		}
		when, err := ToAD(year, of.Month, day)
		if err != nil {
			continue
		}
		if !when.Before(from) {
			return when, nil
		}
	}
	return time.Time{}, fmt.Errorf("no anniversary of %s after %s",
		of, from.Format("2006-01-02"))
}
